/*
Classes: Syntax

객체 지향 프로그래밍의 기본 개념은 클래스입니다.

클래스는 인스턴스가 동작하는 방식과 인스턴스에 포함된 정보를 설명하는 일련의 지침입니다.

Java에는 시스템과 같은 미리 정의된 클래스가 있으며 화면에 텍스트를 기록하는 데 사용했지만
프로그램의 사용자 지정 요구 사항에 맞게 자체 클래스를 작성해야 합니다.

다음은 Java 클래스의 정의입니다.

```
public class Car {
// scope of Car class starts after curly brace , 자동차 클래스의 범위는 중괄호 뒤에 시작됩니다.

  public static void main(String[] args) {
    // scope of main() starts after curly brace , main()의 범위는 중괄호 다음에 시작합니다.

    // program tasks , 프로그램 작업

  }
  // scope of main() ends after curly brace , main()의 범위는 중괄호 뒤에 끝납니다.

}
// scope of Car class ends after curly brace , 자동차 클래스의 범위는 중괄호 뒤에 끝납니다.
```

이 예제는 Car라는 클래스를 정의합니다.
public은 다른 클래스가 이 클래스와 상호 작용할 수 있도록 하는 액세스 수준 한정자입니다.
지금은 모든 수업이 공개됩니다.

이 클래스에는 프로그램이 수행하는 작업을 나열하는 main() 메서드가 있습니다.
main()은 컴파일된 Car.class 파일을 실행할 때 실행됩니다.

 */